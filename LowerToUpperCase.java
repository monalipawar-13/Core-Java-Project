//Write a java program to convert given string in lower and uppercase

package string;

public class LowerToUpperCase 
{
	public static void main(String[] args) 
	{
		String s1="Have a Nice Day";
		
		System.out.println("Lowercase Operation : " +s1.toLowerCase());
		
		System.out.println("Lowercase Operation : " +s1.toUpperCase());
		
	}

}
