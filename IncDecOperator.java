package operator;

public class IncDecOperator 
{

	public static void main(String[] args) 
	{
		int a=10;
		int b=20;
		
		System.out.println("Value of a "+a);
		System.out.println("Value of a "+b);
		
		System.out.println("++a   "+(++a));
		System.out.println("b++ "+b++);
		System.out.println("Value of b "+b);
	

	}

}
